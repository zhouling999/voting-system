package pre.vote.model;

public class Vote {

	private int vid;
	private int vname;
	private String content;
	
	public int getVid() {
		return vid;
	}
	public void setVid(int vid) {
		this.vid = vid;
	}
	public int getVname() {
		return vname;
	}
	public void setVname(int vname) {
		this.vname = vname;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
}
